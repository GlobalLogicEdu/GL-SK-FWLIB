int _write(int fd, char *str, int len)
{
	if (fd != 1) return 0;

	while (len)
	{
		if (str == NULL) return 0;
		USART_SendData(USART3, *str++);
		while (USART_GetFlagStatus(USART3, USART_FLAG_TC) == RESET);
		--len;
	}
}

